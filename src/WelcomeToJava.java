
public class WelcomeToJava
{
	public static void main(String[] args)
	{
		int count = 0; //Initial value of count is 0
		while (count < 20) // as long as count is less than 20
		{
			System.out.println("Welcome To Java!"); // Print: Welcome To Java!
			count++; //increment count by one, until (false.) it reaches the max value 19 which is less than 20.
		}
	}
}
